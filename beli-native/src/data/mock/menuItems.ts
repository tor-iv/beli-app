// Helper function to get menu items for a restaurant
export const getMenuItemsForRestaurant = (restaurantId: string): MenuItem[] => {
  const menuItemIds = restaurantMenus[restaurantId] || [];
  return allMenuItems.filter(item => menuItemIds.includes(item.id));
};

// Helper function to get a menu item by ID
export const getMenuItemById = (itemId: string): MenuItem | undefined => {
  return allMenuItems.find(item => item.id === itemId);
};
